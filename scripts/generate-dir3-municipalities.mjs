import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const defaultOutputPath = path.join(
  rootDir,
  "supabase",
  "seed",
  "2026-05-12_dir3_municipalities.sql",
);
const exampleInputPath = path.join(
  "data",
  "raw",
  "dir3",
  "municipios_es_dir3.csv",
);

const requiredColumns = [
  "dir3_code",
  "ine_municipality_code",
  "name",
  "name_search",
  "slug",
  "province_code",
  "province_name",
  "autonomous_community_code",
  "autonomous_community_name",
  "country_code",
  "source_name",
  "source_file",
  "source_downloaded_at",
];

function buildUsageMessage() {
  return [
    "Usage: node scripts/generate-dir3-municipalities.mjs <input-csv> [output-sql] [--chunk-size=N] [--chunk-dir=PATH]",
    "",
    "Input CSV must be a normalized DIR3 municipalities file with columns:",
    requiredColumns.join(", "),
    "",
    `Example: node scripts/generate-dir3-municipalities.mjs ${exampleInputPath}`,
  ].join("\n");
}

const args = process.argv.slice(2);
const positionalArgs = args.filter((arg) => !arg.startsWith("--"));

if (!positionalArgs[0]) {
  console.error(buildUsageMessage());
  process.exit(1);
}

const inputPath = path.resolve(positionalArgs[0]);
const outputPath = positionalArgs[1] ? path.resolve(positionalArgs[1]) : defaultOutputPath;
const chunkSizeArg = args.find((arg) => arg.startsWith("--chunk-size="));
const chunkDirArg = args.find((arg) => arg.startsWith("--chunk-dir="));
const chunkSize = chunkSizeArg ? Number(chunkSizeArg.split("=")[1]) : 0;
const chunkDir = chunkDirArg
  ? path.resolve(chunkDirArg.split("=")[1])
  : path.join(path.dirname(outputPath), "chunks");

function parseCsvLine(line) {
  const values = [];
  let currentValue = "";
  let isQuoted = false;

  for (let index = 0; index < line.length; index += 1) {
    const character = line[index];
    const nextCharacter = line[index + 1];

    if (character === '"' && isQuoted && nextCharacter === '"') {
      currentValue += '"';
      index += 1;
      continue;
    }

    if (character === '"') {
      isQuoted = !isQuoted;
      continue;
    }

    if (character === "," && !isQuoted) {
      values.push(currentValue);
      currentValue = "";
      continue;
    }

    currentValue += character;
  }

  values.push(currentValue);

  return values;
}

function parseCsv(source) {
  const lines = source.replace(/^\uFEFF/, "").split(/\r?\n/).filter(Boolean);
  const headers = parseCsvLine(lines[0]);
  const missingColumns = requiredColumns.filter((column) => !headers.includes(column));

  if (missingColumns.length > 0) {
    throw new Error(`Missing required CSV columns: ${missingColumns.join(", ")}`);
  }

  return lines.slice(1).map((line) => {
    const values = parseCsvLine(line);
    const row = {};

    headers.forEach((header, index) => {
      row[header] = values[index] ?? "";
    });

    return row;
  });
}

function normalizeSearchText(value) {
  return String(value ?? "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, " ")
    .trim()
    .replace(/\s+/g, " ");
}

function quoteSql(value) {
  const normalizedValue = String(value ?? "").trim();

  if (!normalizedValue) {
    return "null";
  }

  return `'${normalizedValue.replaceAll("'", "''")}'`;
}

function rowToSqlValues(row) {
  const searchText = normalizeSearchText(
    [
      row.name,
      row.name_search,
      row.province_name,
      row.autonomous_community_name,
      row.ine_municipality_code,
      row.dir3_code,
    ].join(" "),
  );

  return `(${[
    quoteSql(row.dir3_code),
    quoteSql(row.ine_municipality_code),
    quoteSql(row.name),
    quoteSql(row.name_search || normalizeSearchText(row.name)),
    quoteSql(row.slug),
    quoteSql(row.province_code),
    quoteSql(row.province_name),
    quoteSql(row.autonomous_community_code),
    quoteSql(row.autonomous_community_name),
    quoteSql(row.country_code || "ES"),
    quoteSql(row.source_name),
    quoteSql(row.source_file),
    quoteSql(row.source_downloaded_at),
    quoteSql(searchText),
  ].join(", ")})`;
}

function buildInsertChunks(rows, chunkSize = 500) {
  const chunks = [];

  for (let index = 0; index < rows.length; index += chunkSize) {
    const chunk = rows.slice(index, index + chunkSize);

    chunks.push(`insert into dir3_municipality_seed (
  dir3_code,
  municipality_code,
  name,
  name_search,
  slug,
  province_code,
  province_name,
  autonomous_community_code,
  autonomous_community_name,
  country_code,
  source_name,
  source_file,
  source_downloaded_at,
  search_text
)
values
${chunk.map(rowToSqlValues).join(",\n")};`);
  }

  return chunks;
}

function buildSeedSql(rows, metadata = {}) {
  const chunkComment = metadata.chunkLabel ? `\n-- Chunk: ${metadata.chunkLabel}` : "";

  return `-- Generated by scripts/generate-dir3-municipalities.mjs.
-- Source: official DIR3 Catalogo de Localidades, municipality rows only (Cod. Entidad = 01).
-- Row count: ${rows.length}${chunkComment}

begin;

create temp table dir3_municipality_seed (
  dir3_code text primary key,
  municipality_code text not null,
  name text not null,
  name_search text not null,
  slug text not null,
  province_code text not null,
  province_name text not null,
  autonomous_community_code text not null,
  autonomous_community_name text not null,
  country_code text not null,
  source_name text not null,
  source_file text not null,
  source_downloaded_at date not null,
  search_text text not null
) on commit drop;

${buildInsertChunks(rows).join("\n\n")}

update public.cities as cities
set
  name = seed.name,
  slug = seed.slug,
  country_code = seed.country_code,
  province_code = seed.province_code,
  province_name = seed.province_name,
  autonomous_community_code = seed.autonomous_community_code,
  autonomous_community_name = seed.autonomous_community_name,
  municipality_code = seed.municipality_code,
  dir3_code = seed.dir3_code,
  source_name = seed.source_name,
  source_file = seed.source_file,
  name_search = seed.name_search,
  search_text = seed.search_text,
  place_type = 'municipality',
  is_active = true,
  updated_at = now()
from dir3_municipality_seed as seed
where
  cities.dir3_code = seed.dir3_code
  or (
    cities.municipality_code = seed.municipality_code
    and coalesce(cities.province_code, seed.province_code) = seed.province_code
  )
  or (
    cities.dir3_code is null
    and cities.municipality_code is null
    and cities.province_code = seed.province_code
    and cities.name_search = seed.name_search
    and not exists (
      select 1
      from public.cities as duplicate_city
      where duplicate_city.id <> cities.id
        and duplicate_city.province_code = cities.province_code
        and duplicate_city.name_search = cities.name_search
    )
  )
  or (
    cities.dir3_code is null
    and cities.municipality_code is null
    and cities.province_code is null
    and seed.province_code = '08'
    and cities.name in ('Sitges', 'Sant Pere de Ribes', 'Vilanova i la Geltrú')
    and cities.name = seed.name
  );

insert into public.cities (
  name,
  slug,
  country_code,
  province_code,
  province_name,
  autonomous_community_code,
  autonomous_community_name,
  municipality_code,
  dir3_code,
  source_name,
  source_file,
  name_search,
  search_text,
  place_type,
  is_active,
  updated_at
)
select
  seed.name,
  seed.slug,
  seed.country_code,
  seed.province_code,
  seed.province_name,
  seed.autonomous_community_code,
  seed.autonomous_community_name,
  seed.municipality_code,
  seed.dir3_code,
  seed.source_name,
  seed.source_file,
  seed.name_search,
  seed.search_text,
  'municipality',
  true,
  now()
from dir3_municipality_seed as seed
where not exists (
  select 1
  from public.cities as cities
  where cities.dir3_code = seed.dir3_code
    or cities.municipality_code = seed.municipality_code
);

do $$
declare
  current_sequence text;
  next_value bigint;
begin
  select pg_get_serial_sequence('public.cities', 'id')
  into current_sequence;

  if current_sequence is not null then
    select greatest(coalesce(max(id), 0) + 1, 1)
    into next_value
    from public.cities;

    perform setval(current_sequence::regclass, next_value, false);
  end if;
end;
$$;

commit;
`;
}

if (!existsSync(inputPath)) {
  console.error(
    [
      `DIR3 input CSV not found: ${inputPath}`,
      `Place the normalized CSV at ${exampleInputPath} or pass its path as the first argument.`,
      "",
      buildUsageMessage(),
    ].join("\n"),
  );
  process.exit(1);
}

const csvSource = readFileSync(inputPath, "utf8");
const rows = parseCsv(csvSource);

mkdirSync(path.dirname(outputPath), { recursive: true });
writeFileSync(outputPath, buildSeedSql(rows), "utf8");

console.log(`Generated ${rows.length} DIR3 municipality upserts at ${outputPath}`);

if (Number.isInteger(chunkSize) && chunkSize > 0) {
  mkdirSync(chunkDir, { recursive: true });

  const totalChunks = Math.ceil(rows.length / chunkSize);

  for (let chunkIndex = 0; chunkIndex < totalChunks; chunkIndex += 1) {
    const chunkRows = rows.slice(chunkIndex * chunkSize, (chunkIndex + 1) * chunkSize);
    const chunkNumber = String(chunkIndex + 1).padStart(2, "0");
    const chunkPath = path.join(
      chunkDir,
      `2026-05-12_dir3_municipalities_part_${chunkNumber}_of_${String(totalChunks).padStart(2, "0")}.sql`,
    );

    writeFileSync(
      chunkPath,
      buildSeedSql(chunkRows, {
        chunkLabel: `${chunkIndex + 1} of ${totalChunks}`,
      }),
      "utf8",
    );
  }

  console.log(`Generated ${totalChunks} chunked DIR3 seed files at ${chunkDir}`);
}
